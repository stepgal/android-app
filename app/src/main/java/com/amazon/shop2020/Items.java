package com.amazon.shop2020;

class Items
{
    String id;
    String title;
    String description;
    float cost;

    public Items()
    {
    }

    public String getId()
    {
        return id;
    }

    public String getTitle()
    {
        return title;
    }

    public String getDescription()
    {
        return description;
    }

    public float getCost()
    {
        return cost;
    }
}
